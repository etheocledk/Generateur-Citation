async function date() {
    let datenew = new Date()
    let date = datenew.toLocaleString('fr-FR', {
        year: 'numeric',
        month: 'long',
        day: 'numeric',
    })
    let viewtime = document.querySelector('.my-date')
    viewtime.innerHTML = `${date}`
}

