/*function pour changer la voix*/ 
let volumeoff = document.querySelector('.citation-text__icon__left .mdi-volume-off')
let volume = document.querySelector('.mdi-volume-high')
volume.addEventListener('click', () => {
    let speechText = document.querySelector('.proverbe').textContent
    let autoreNom = document.querySelector('.author').textContent
    let speech = new SpeechSynthesisUtterance(`${speechText}  ${autoreNom}`)
    let voices
    voices = window.speechSynthesis.getVoices()
    setTimeout(() => {
        let vare = document.querySelector('.gt-selected').firstChild.querySelector(".gt-lang-code").innerText
        
        if(vare==="FR"){
            speech.voice = voices.filter((post)=>post.lang==="fr")[0]
        }
    
        else if(vare==="EN"){
            speech.voice = voices.filter((post)=>post.lang==="en")[0]
        }
    
        else if(vare==="IT"){
            speech.voice = voices.filter((post)=>post.lang==="it")[0]
        }
    
        else if(vare==="ES"){
            speech.voice = voices.filter((post)=>post.lang==="es")[0]
        }
    
        else if(vare==="DE"){
            speech.voice = voices.filter((post)=>post.lang==="de")[0]
        } 
        
        speech.pitch=1//hauteur de la voix(0 à 2)
        speech.rate=1//vitesse de la voix (0.5 à 2)
        speech.volume=1//volume de la voix(0 à 1)
        
        speechSynthesis.speak(speech);

    }, 1000);

    /*exécuté une fonction à la fin*/
    speech.onend = function(event){
        console.log(event.elapsedTime);
         volume.classList.remove('active')
         volumeoff.classList.remove('active')
     }

  
    if (!volume.classList.contains('active')) {
        volume.classList.add('active')
    }
    if (!volumeoff.classList.contains('active')) {
        volumeoff.classList.add('active')
    }
})

volumeoff.addEventListener('click', () => {
    speechSynthesis.cancel()
    if (volume.classList.contains('active')) {
        volume.classList.remove('active')
    }
    if (volumeoff.classList.contains('active')) {
        volumeoff.classList.remove('active')
    }
})

